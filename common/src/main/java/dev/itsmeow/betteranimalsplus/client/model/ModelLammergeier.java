    @Override
    public void setupAnim(T entityIn, float limbSwing, float limbSwingAmount, float ageInTicks, float netHeadYaw, float headPitch) {
        EntityLammergeier lammergeier = (EntityLammergeier) entityIn;
        boolean isFlying = lammergeier.getFlying();

        if (isFlying) {
            this.rWing01.zRot = Mth.cos(ageInTicks * 0.3F) * (float) Math.PI * 0.25F;

            if ((Math.abs(lammergeier.getDeltaMovement().y()) > 0 && (Math.abs(lammergeier.getDeltaMovement().x()) > 0.05 || Math.abs(lammergeier.getDeltaMovement().z()) > 0.05))
                    || Math.abs(lammergeier.getDeltaMovement().y()) > 0.25) {
                if (Math.abs(lammergeier.getDeltaMovement().y() + lammergeier.lastMotionY) > 0.05 && lammergeier.getDeltaMovement().y() < 0) {
                    this.rWing01.zRot = Mth.cos(225 * 0.3F) * (float) Math.PI * 0.25F;
                }
            }
            this.body.xRot = (float) Mth.clampedLerp(lammergeier.lastRotX, lammergeier.rotX, limbSwing);
            this.lWing01.zRot = -this.rWing01.zRot;
            this.rWing02.zRot = this.rWing01.zRot * 0.5F;
            this.lWing02.zRot = -this.rWing01.zRot * 0.5F;
            if (!this.lastFlying) {
                this.switchToFlight();
            }
        } else {
            if (this.lastFlying) {
                this.switchToWalk();
            }
            boolean flag = entityIn.getFallFlyingTicks() > 4;
            float f = 1.0F;

            if (flag) {
                f = (float) (entityIn.getDeltaMovement().x() * entityIn.getDeltaMovement().x() + entityIn.getDeltaMovement().y() * entityIn.getDeltaMovement().y() + entityIn.getDeltaMovement().z() * entityIn.getDeltaMovement().z());
                f = f / 0.2F;
                f = f * f * f;
            }

            if (f < 1.0F) {
                f = 1.0F;
            }

            this.rLeg01.xRot = Mth.cos(limbSwing * 0.6662F) * 1.4F * limbSwingAmount / f;
            this.lLeg01.xRot = Mth.cos(limbSwing * 0.6662F + (float) Math.PI) * 1.4F * limbSwingAmount / f;

        }

        if (lammergeier.isInSittingPose()) {
            this.head.xRot = 0;
        } else {
            this.head.xRot = -0.81F;
        }

        this.lastFlying = isFlying;
    }

    public void setRotateAngle(ModelPart modelRenderer, float x, float y, float z) {
        modelRenderer.xRot = x;
        modelRenderer.yRot = y;
        modelRenderer.zRot = z;
    }
