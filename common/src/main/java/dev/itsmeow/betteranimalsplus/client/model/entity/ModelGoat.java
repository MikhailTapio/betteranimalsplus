    @Override
    public void setupAnim(T entityIn, float limbSwing, float limbSwingAmount, float ageInTicks, float netHeadYaw, float headPitch) {
        this.quadriped(lLeg01, lArm01, rLeg01, rArm01, limbSwing * 0.8665F, limbSwingAmount * 1.5F);
        this.headYaw(neck, netHeadYaw, 0.5F, 0F);
        if (entityIn instanceof EntityGoat) {
            EntityGoat goat = (EntityGoat) entityIn;
            this.neck.xRot = goat.getHeadPitch(); // Ram attack
            if (!goat.isAttackingFromServer()) {
                float eatTime = goat.getEatTime();
                if (eatTime > 0) {
                    this.neck.xRot = rad(40F);
                    this.head.xRot = rad(-65F);
                    this.lowerJaw.xRot = rad(eatTime % 20F) + 0.1F;
                } else {
                    resetGoat();
                }
            } else {
                resetGoat();
            }
        }
    }

    private void resetGoat() {
        this.head.xRot = -0.7853981633974483F;
        this.lowerJaw.xRot = 0F;
    }

